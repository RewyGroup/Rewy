export function SvgNumberPicker(object){
    
  switch(object.type){
    case "titel":
        return checkStateForTitle(object.state);
        break;
    case "kategori":
        return checkStateForCategory(object.state);
        break;       
    case "taggar":
        return checkStateForSubCategory(object.state);
        break;      
    default:
        return checkStateForDescription(object.state);
        break;
}

}

const checkStateForTitle = (state) =>{

    switch(state){
        case "ACTIVE":
            return titleActive;
            break;
        case "NEUTRAL":
            return titleNeutral;
            break;       
        default:
            return correct;
            break;
    }
};

